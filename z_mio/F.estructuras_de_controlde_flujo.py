# Estructura if
numero = 10
if numero > 5:
    print("El número es mayor que 5")
    
# Estructura if-else
numero = 3
if numero > 5:
    print("El número es mayor que 5")
else:
    print("El número es menor o igual que 5")
    
# Estructura if-elif-else
numero = 7
if numero < 5:
    print("El número es menor que 5")
elif numero < 10:
    print("El número es mayor o igual que 5 y menor que 10")
else:
    print("El número es mayor o igual que 10")
