#Operadores de pertenencia(in / not in)

##Es sencible a las mayusculas y minusculas


#contatenar(4)(true : in)

nombre_completo = "Aaron"
bienvenida = f"Hola {nombre_completo} ¿Como estas?"
del nombre_completo
print("Aaron" in bienvenida) #True

#contatenar(4)(false : not in)

nombre = "Aaron"
bienvenida_al_usuario = f"Hola {nombre} ¿Como estas?"

print("Aaron" not in bienvenida_al_usuario) #False