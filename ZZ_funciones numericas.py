#abs(): Esta función devuelve el valor absoluto de un número, es decir, el valor sin signo. Por ejemplo:

numero = -10
print(abs(numero)) # devuelve 10


#pow(): Esta función devuelve la potencia de un número. Recibe dos parámetros: la base y el exponente. Por ejemplo:


base = 2
exponente = 3
print(pow(base, exponente)) # devuelve 8



#round(): Esta función redondea un número al entero más cercano. Recibe dos parámetros: el número a redondear y 
#el número de decimales a los que se quiere redondear. Por ejemplo:

numero = 3.1416
print(round(numero)) # devuelve 3
print(round(numero, 2)) # devuelve 3.14



#max() y min(): Estas funciones devuelven el valor máximo y mínimo de una serie de números. Por ejemplo:

numeros = [3, 5, 2, 8, 1]
print(max(numeros)) # devuelve 8
print(min(numeros)) # devuelve 1


#sum(): Esta función devuelve la suma de los valores de una serie de números. Por ejemplo:

numeros = [3, 5, 2, 8, 1]
print(sum(numeros)) # devuelve 19

#int(): Esta función convierte un valor a un entero. Por ejemplo:

numero = 3.1416
print(int(numero)) # devuelve 3